const Datatable = () => {
  const [data, setData] = useState(userRows);

  const handleDelete = (id) => {
    setData(data.filter((item) => item.id !== id));
  };

  const getDashboardFn = async () => {
    const response = await getDashboard("Delhi");
    const families = response.data.families;

    families.forEach((item)=> {
      item.id = item._id;
      item.rice = item.rationTaken[0].quantity;
      item.oil = item.rationTaken[1].quantity;
      item.sugar = item.rationTaken[2].quantity;
      item.pulses = Math.floor(Math.random()*100+1);
    });
    setData(families);
}

useEffect(() => {
  getDashboardFn();
}, [])

  const actionColumn = [
    {
      field: "action",
      headerName: "Action",
      width: 200,
      renderCell: (params) => {
        return (
          <div className="cellAction">
            <Link to="/users/test" style={{ textDecoration: "none" }}>
              <div className="viewButton">View</div>
            </Link>
            <div
              className="deleteButton"
              onClick={() => handleDelete(params.row.id)}
            >
              Delete
            </div>
          </div>
        );
      },
    },
  ];
  return (
    <div className="datatable">
      <div className="datatableTitle">
        Family's database
        <Link to="/users/new" className="link">
          Add New
        </Link>
      </div>
      <DataGrid
        className="datagrid"
        rows={data}
        columns={userColumns.concat(actionColumn)}
        pageSize={9}
        rowsPerPageOptions={[9]}
        checkboxSelection
      />
    </div>
  );
};

export default Datatable;
